/**
 * GitHub Monitor Scheduler — Auto-registers a cron task that polls
 * subscribed repositories for new issues and generates replies.
 *
 * The task runs every 5 minutes. Its prompt instructs the model to:
 * 1. Check each monitored repo for open issues via `gh api`
 * 2. Skip issues already in ~/.claude/github_replied.json
 * 3. Post a helpful reply and record the issue ID.
 */

import * as fs from 'fs/promises'
import * as path from 'path'
import * as os from 'os'
import { CronService } from './cronService.js'
import { getSecureStorage } from '../../utils/secureStorage/index.js'

const MONITOR_TASK_NAME = 'GitHub Issue Monitor'
const MONITOR_CRON = '*/5 * * * *'

function getConfigDir(): string {
  return process.env.CLAUDE_CONFIG_DIR || path.join(os.homedir(), '.claude')
}

function getSettingsPath(): string {
  return path.join(getConfigDir(), 'settings.json')
}

function getRepliedLogPath(): string {
  return path.join(getConfigDir(), 'github_replied.json')
}

async function readJsonFile<T>(filePath: string): Promise<T | null> {
  try {
    const raw = await fs.readFile(filePath, 'utf-8')
    return JSON.parse(raw) as T
  } catch {
    return null
  }
}

function getStoredCredentials(): { pat: string; username: string; avatar?: string } | null {
  try {
    const storage = getSecureStorage()
    const data = storage.read()
    if (!data) return null
    const creds = data['githubCredentials']
    if (
      creds &&
      typeof creds === 'object' &&
      'pat' in creds &&
      typeof (creds as Record<string, unknown>).pat === 'string'
    ) {
      return creds as { pat: string; username: string; avatar?: string }
    }
    return null
  } catch {
    return null
  }
}

function buildPrompt(
  repos: Array<{ owner: string; repo: string }>,
  repliedLog: Record<string, string>,
): string {
  const repoList = repos.map((r) => `- ${r.owner}/${r.repo}`).join('\n')
  const repliedList =
    Object.keys(repliedLog).length > 0
      ? Object.entries(repliedLog)
          .map(([id, ts]) => `- ${id} (${ts})`)
          .join('\n')
      : '(none yet)'

  return `You are the GitHub Issue Monitor for cc-haha.

Your job is to check monitored repositories for new unresponded issues and post helpful replies.

Monitored repositories:
${repoList}

Already-replied issues (DO NOT reply to these again):
${repliedList}

Instructions:
1. For each monitored repository, run: gh api repos/{owner}/{repo}/issues?state=open&per_page=10
2. For each issue, check if it has comments: gh api repos/{owner}/{repo}/issues/{number}/comments
3. Only reply to issues that:
   - Have NO comments (or only the original poster has commented)
   - Are NOT in the already-replied list above
4. Read the repository's README.md if available to understand the project context
5. Generate a friendly, professional reply in the same language as the issue
6. Post the reply: gh api -X POST repos/{owner}/{repo}/issues/{number}/comments -f body="your reply"
7. After replying to an issue, record it by appending to ~/.claude/github_replied.json:
   {"owner/repo#number": "ISO timestamp"}

Reply guidelines:
- For bug reports: thank the reporter, ask for reproduction steps / environment info
- For feature requests: thank them, explain if it will be considered
- For questions: provide a helpful answer or point to documentation
- NEVER be rude, dismissive, or robotic
- End with: "— This reply was generated by Claude Code Haha. If inaccurate, please let us know."`
}

export class GithubMonitorScheduler {
  private cronService: CronService

  constructor() {
    this.cronService = new CronService()
  }

  async start(): Promise<void> {
    // 1. Set GH_TOKEN env var so child CLI processes can use gh api
    const creds = getStoredCredentials()
    if (creds?.pat) {
      process.env.GH_TOKEN = creds.pat
      console.log('[GitHubMonitor] GH_TOKEN set from secure storage')
    } else {
      console.log('[GitHubMonitor] No GitHub token found, monitor will use gh auth status')
    }

    // 2. Read user settings for monitored repos
    const settings = await readJsonFile<Record<string, unknown>>(getSettingsPath())
    const repos =
      (settings?.githubMonitoredRepos as Array<{
        owner: string
        repo: string
        autoReply: boolean
      }>) || []
    const activeRepos = repos.filter((r) => r.autoReply)

    // 3. Find existing monitor task by name
    const tasks = await this.cronService.listTasks()
    const existing = tasks.find((t) => t.name === MONITOR_TASK_NAME)

    if (activeRepos.length === 0) {
      console.log('[GitHubMonitor] No active repositories to monitor')
      if (existing && existing.enabled !== false) {
        await this.cronService.updateTask(existing.id, { enabled: false })
        console.log('[GitHubMonitor] Disabled existing monitor task')
      }
      return
    }

    // 4. Build prompt with current repo list + replied log
    const repliedLog = (await readJsonFile<Record<string, string>>(getRepliedLogPath())) || {}
    const prompt = buildPrompt(activeRepos, repliedLog)

    if (existing) {
      // Update existing task
      const updates: Record<string, unknown> = { prompt, enabled: true }
      if (existing.cron !== MONITOR_CRON) {
        updates.cron = MONITOR_CRON
      }
      await this.cronService.updateTask(existing.id, updates)
      console.log('[GitHubMonitor] Updated monitor task')
    } else {
      // Create new task
      await this.cronService.createTask({
        name: MONITOR_TASK_NAME,
        description: 'Automatically monitor and reply to GitHub issues',
        cron: MONITOR_CRON,
        prompt,
        recurring: true,
        permanent: true,
        enabled: true,
      })
      console.log('[GitHubMonitor] Created monitor task')
    }
  }
}

export const githubMonitorScheduler = new GithubMonitorScheduler()
