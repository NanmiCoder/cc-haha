import { registerBundledSkill } from '../bundledSkills.js'

export function registerGithubResponderSkill(): void {
  registerBundledSkill({
    name: 'github-responder',
    description: 'Monitor GitHub repositories and auto-reply to new issues.',
    aliases: ['gh-responder'],
    userInvocable: true,
    allowedTools: ['Bash(gh:*)', 'Read', 'Write'],
    async getPromptForCommand(args) {
      const parts: string[] = [
        `You are the GitHub Issue Responder.

Your job is to check monitored repositories for new unresponded issues and post helpful replies.

Instructions:
1. Read ~/.claude/settings.json to get the list of monitored repositories (githubMonitoredRepos)
2. For each repository with autoReply=true, run: gh api repos/{owner}/{repo}/issues?state=open&per_page=10
3. For each issue, check if it has comments: gh api repos/{owner}/{repo}/issues/{number}/comments
4. Read ~/.claude/github_replied.json to check which issues have already been replied to
5. Only reply to issues that:
   - Have NO comments (or only the original poster has commented)
   - Are NOT in the already-replied list
6. Read the repository's README.md if available to understand the project context
7. Generate a friendly, professional reply in the same language as the issue
8. Post the reply: gh api -X POST repos/{owner}/{repo}/issues/{number}/comments -f body="your reply"
9. After replying to an issue, record it by appending to ~/.claude/github_replied.json:
   {"owner/repo#number": "ISO timestamp"}

Reply guidelines:
- For bug reports: thank the reporter, ask for reproduction steps / environment info
- For feature requests: thank them, explain if it will be considered
- For questions: provide a helpful answer or point to documentation
- NEVER be rude, dismissive, or robotic
- End with: "— This reply was generated by Claude Code Haha. If inaccurate, please let us know."`,
      ]

      if (args?.trim()) {
        parts.push(`## User Request\n\n${args}`)
      }

      return [{ type: 'text', text: parts.join('\n\n') }]
    },
  })
}
